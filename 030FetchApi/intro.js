// WHat is the Fetch API
// 1. Built-in web standard API for fetching resources
// 2. Contains interfaces for Requests, Responses, and their Headers
// 3. Successor to the older XMLHttpRequest function
// 4. Available in most JS environments like the Browser, NodeJS, etc.

// Main parts of Fetch API
// 1. fetch() -> Function we'll use to fetch resources
// 2. Headers -> Customize request/response "headers" to take different actions (optional)
// 3. Request -> The Request Object (usually made for us)
// 4. Response -> The Response Object (usually made for us)

// NOTE: fetch API returns a promise
